depth = 0


def seidel(A, b, eps):
    """Разработать программу для численного решения СЛАУ методом Зейделя
    с организацией хранения сильно разреженной матрицы большой размерности"""

    n = len(A)
    x = [0.0 for i in range(n)]


    converge = False
    while not converge :
        global depth
        depth +=1
        x_new = np.copy(x)
        for i in range(n):
            s1 = abs(sum(A[i][j] * x_new[j] for j in range(i)))
            s2 = abs(sum(A[i][j] * x[j] for j in range(i + 1, n)))
            x_new[i] = (b[i] - s1 - s2) / A[i][i]
        #if depth ==0: break
        converge = sqrt(sum((x_new[i] - x[i]) ** 2 for i in range(n))) <= eps
        #print(x_new)
        x = x_new

    return x


